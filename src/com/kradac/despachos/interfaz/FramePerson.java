    private void tblPersonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPersonMousePressed
        int intClicks = evt.getClickCount();
        if (intClicks == 1) {
            int rowSelected = tblPerson.getSelectedRow();
            String cedula = tblPerson.getValueAt(rowSelected, 0).toString();

            Person p = Principal.listPerson.getPersonByCedula(cedula);
            txtCedula.setText(p.getCedula());
            txtName.setText(p.getName());
            txtLastName.setText(p.getLastname());
            txtPhone.setText(p.getPhone());
            txtEmail.setText(p.getEmail());
            txtDirection.setText(p.getDirection());
            txtNumHouse.setText("" + p.getNumHouse());
            txtTypeSangre.setText(p.getTypeSangre());
            cbxStateCivil.setSelectedItem(p.getStateCivil().getStateCivil());
            cbxJobs.setSelectedItem(p.getJob().getJob());
            txtConyugue.setText(p.getConyuge());

            btnRefresh.setEnabled(true);
            btnAdd.setEnabled(false);
            btnDelete.setEnabled(true);
        }
    }//GEN-LAST:event_tblPersonMousePressed

