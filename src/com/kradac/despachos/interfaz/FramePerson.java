    private void loadComponent() {
        modelTablePerson = (DefaultTableModel) tblPerson.getModel();
        for (Person p : Principal.listPerson.getPersons()) {
            modelTablePerson.addRow(changeToArrayPerson(p));
        }

        for (StateCivil sc : Principal.listStateCivil.getStateCivils()) {
            cbxStateCivil.addItem(sc.getStateCivil());
        }

        for (Job j : Principal.listJob.getJobs()) {
            cbxJobs.addItem(j.getJob());
        }
    }

    private String[] changeToArrayPerson(Person person) {
        String[] dataPerson = {
            person.getCedula(),
            person.getName(),
            "" + person.getLastname(),
            "" + person.getPhone()
        };
        return dataPerson;
    }

    private void clear() {
        btnDelete.setEnabled(false);
        btnRefresh.setEnabled(false);
        btnAdd.setEnabled(true);

        txtCedula.setText("");
        txtName.setText("");
        txtLastName.setText("");
        txtPhone.setText("");
        txtEmail.setText("");
        txtDirection.setText("");
        txtNumHouse.setText("");
        txtTypeSangre.setText("");
        txtConyugue.setText("");
        lblImage.setIcon(null);
    }

    private void delete() {
        int rowSelected = tblPerson.getSelectedRow();
        clear();
        if (Principal.listPerson.deletePerson(tblPerson.getValueAt(rowSelected, 0).toString())) {
            modelTablePerson.removeRow(rowSelected);
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo eliminar la Persona de la Lista");
        }
    }

    private void add() {
        if (isValidCedula && isValidName && isValidLastName) {

            Person p = new Person(txtCedula.getText(), txtName.getText().toUpperCase(), txtLastName.getText().toUpperCase(), txtPhone.getText(), txtEmail.getText(),
                    txtDirection.getText(), txtNumHouse.getText(), txtTypeSangre.getText(),
                    Principal.listStateCivil.getStateCivilByState(cbxStateCivil.getSelectedItem().toString()), txtConyugue.getText().toUpperCase(), "",
                    Principal.listJob.getJobByName(cbxJobs.getSelectedItem().toString()));
            int insert = Principal.listPerson.addNewPerson(p);
            if (insert == 0) {
                JOptionPane.showMessageDialog(this, "La persona ya existe.", "ERROR", JOptionPane.ERROR_MESSAGE);
            } else {
                if (insert == 1) {
                    modelTablePerson.addRow(changeToArrayPerson(p));
                    clear();
                } else {
                    System.out.println("Error insertando persona");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Compruebe que los campos Requeridos sean los correctos (Cedula, Nombres, Apellidos)", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

