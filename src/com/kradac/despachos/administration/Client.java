package com.kradac.despachos.administration;

/**
 *
 * @author Dalton
 */
public class Client {
    private String name;
    private String lastname;
    private String phone;
    private String direction;
    private String sector;
    private int code;
    private int numHouse;
    private double latitud;
    private double longitud;
    private String reference;

    public Client(String name, String lastname, String phone, String direction, String sector, int code, int numHouse, double latitud, double longitud, String reference) {        
        this.name = name;
        this.lastname = lastname;
        this.phone = phone;
        this.direction = direction;
        this.sector = sector;
        this.code = code;
        this.numHouse = numHouse;
        this.latitud = latitud;
        this.longitud = longitud;
        this.reference = reference;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the lastname
     */
    public String getLastname() {
        return lastname;
    }

    /**
     * @param lastname the lastname to set
     */
    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }
    
    /**
     * @return the direction
     */
    public String getDirection() {
        return direction;
    }

    /**
     * @param direction the direction to set
     */
    public void setDirection(String direction) {
        this.direction = direction;
    }

    /**
     * @return the sector
     */
    public String getSector() {
        return sector;
    }

    /**
     * @param sector the sector to set
     */
    public void setSector(String sector) {
        this.sector = sector;
    }

    /**
     * @return the code
     */
    public int getCode() {
        return code;
    }

    /**
     * @param code the code to set
     */
    public void setCode(int code) {
        this.code = code;
    }

    /**
     * @return the numHouse
     */
    public int getNumHouse() {
        return numHouse;
    }

    /**
     * @param numHouse the numHouse to set
     */
    public void setNumHouse(int numHouse) {
        this.numHouse = numHouse;
    }

    /**
     * @return the latitud
     */
    public double getLatitud() {
        return latitud;
    }

    /**
     * @param latitud the latitud to set
     */
    public void setLatitud(double latitud) {
        this.latitud = latitud;
    }

    /**
     * @return the longitud
     */
    public double getLongitud() {
        return longitud;
    }

    /**
     * @param longitud the longitud to set
     */
    public void setLongitud(double longitud) {
        this.longitud = longitud;
    }

    /**
     * @return the reference
     */
    public String getReference() {
        return reference;
    }

    /**
     * @param reference the reference to set
     */
    public void setReference(String reference) {
        this.reference = reference;
    }
}
